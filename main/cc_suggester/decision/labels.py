def caption_for(event_id: str, language: str) -> str:
    """Return a caption label for an event and language."""

    values = LABELS.get(event_id, {})
    if language in values:
        return values[language]
    if "en" in values:
        return values["en"]
    return f"[{event_id.replace('_', ' ')}]"
